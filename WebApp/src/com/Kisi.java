package com;

public class Kisi {
	String isim;
	int yas;
	
	public Kisi(String isim, int yas) {
		super();
		this.isim = isim;
		this.yas = yas;
	}
	public String getIsim() {
		return isim;
	}
	public int getYas() {
		return yas;
	}
	public void setIsim(String isim) {
		this.isim = isim;
	}
	public void setYas(int yas) {
		this.yas = yas;
	}
	
	
}
